inline int power(int a, int b)
{
    int x = 1;
    while (b)
    {
        if (b & 1)
            x *= a;
        a *= a;
        b >>= 1;
    }
    return x;
}
vi getbinary(int n)
{
    vector<int> ans;
    if (n == 0)
    {
        ans.push_back(0);
        return ans;
    }
    while (n > 0)
    {
        int rem = n % 2;
        ans.push_back(rem);
        n = n / 2;
    }
    return ans;
}
void solve()
{
    ll n;
    cin >> n;
    vi arr(n,0);
    ordered_set s;
    for(int i=0;i<n;i++)
    {
        cin>>arr[i];
    }
    int ans=0;
    s.insert(arr[0]);
    for(int i=1;i<n;i++)
    {
       s.insert(arr[i]);
       int x=s.order_of_key(arr[i]+1);
       ans=ans+s.size()-x;
    }
    cout<<ans<<"\n";
}
